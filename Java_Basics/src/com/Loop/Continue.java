package com.Loop;

public class Continue {
	
	public static void main(String[] args) {
		
		for (int i = 0; i <=20; i++) {
			
			if(i==5)
			{
				
				continue;
			}
			if(i==15)
			{
				
				continue;
			}
			if(i==16)
			{
				
				continue;
			}
            System.out.println(i);			
		}
	}
}
