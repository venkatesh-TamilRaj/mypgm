package com.Loop;

public class Pattern {
	
	
public static void main(String[] args) {
	
	for (int i = 1; i <=3; i++) {
		
		
		for (int j = i; j <4; j++) {
			
			System.out.print(" ");
			
		
		}
		for (int k = 0; k < i*2; k++) {
			System.out.print("*");
		}
		
		System.out.println();
		
	}
	}


}
